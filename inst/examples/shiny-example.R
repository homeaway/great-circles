library(shiny)
library(greatCircles)
library(htmlwidgets)



server = function(input, output) {
  output$arcs <- rendergreatCircles(greatCircles(
    data.frame(longitude.start=c(138.6,145.0181,138.6,153.1693),
               latitude.start = c(-34.92862, -37.77931,-34.92862, -27.65875),
               longitude.finish = c(-73.928164, -73.946490, 2.336655, -73.919071),
               latitude.finish = c(40.76352, 40.78027, 48.88156, 40.75708)
    )
  ))
  output$foo = renderText({"Hello World"})
}

ui = shinyUI(fluidPage(

      greatCirclesOutput("arcs")


))

shinyApp(ui = ui, server = server)

